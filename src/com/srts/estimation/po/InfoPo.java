package com.srts.estimation.po;

public class InfoPo {
	private String id;
	private String name;
	public InfoPo(){}
	public InfoPo(String id, String name) {
		this.id = id;
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
